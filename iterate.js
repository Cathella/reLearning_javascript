// Iterate with JavaScript While Loops
var myArray = [];
var i = 5;
while(i >= 0) {
  myArray.push(i);
  i--;
}
console.log(myArray)

// Iterate with JavaScript For Loops
var myArray = [];
for (var i = 1; i <= 5; i++) {
  myArray.push(i)
}
console.log(myArray)

// Iterate Odd Numbers With a For Loop
var myArray = [];
for (var i = 1; i <= 9; i += 2) {
  myArray.push(i);
}
console.log(myArray);

